# -*- encoding: utf-8 -*-

"""
Command-Line Interface to Manage LLM Essential Function Code(s)
===============================================================

Module's command-line utility tool to manage essential functions that
are being regularly used by LLM tools for fine control and refine the
content generated by LLM models. The command line is developed using
:mod:`argparse` which is a standard Python package.

.. code-block:: shell

    $ pip install polyskills
    $ polyskills --help # show help documentation

The CLI tool is developed to manage LLM essential functions - like
``SKILLS.md``, ``AGENTS.md`` etc. required to refine the contents.
"""

import sys
import argparse

from polyskills.remote.sources import ValidSources


def buildParser() -> argparse.ArgumentParser:
    """
    Constructs the :func:`argparse.parser` for the :mod:`polyskills`
    CLI tools, that provides sub-parsers with required mutually
    exclusive groups for library management from remote sources.
    """

    parser = argparse.ArgumentParser(
        prog = "polyskills", description = (
            "Manage portable LLM skills hosted on any source controlled "
            "remote repository for LLM tools (like Claude Code, CodeX)."
        )
    )

    subparser = parser.add_subparsers(
        dest = "command", required = True, metavar = "COMMAND"
    )

    # ? Creating Subparsers:: SOURCES - List Available Sources
    sources = subparser.add_parser(
        "sources", help = (
            "List the available (or supported) remote sources, and exit."
        )
    )
    sources.set_defaults(
        func = lambda : "Available Sources:\n" + "\t".join([
            f"\t>> {str(idx + 1).zfill(2)}. {mem.name} - {mem.value}"
            for idx, mem in enumerate(ValidSources)
        ])
    )

    # ? Creating Subparsers:: MANAGE - Manage Remote Library
    manager = subparser.add_parser(
        "manager", help = (
            "Main method to manage skills, agents, etc. for a LLM "
            "tool, from remote sources, i.e., to fetch and/or update "
            "the library content in different projects or systems. "
            "Check 'polyskills manage --help' for more details."
        )
    )

    manager.add_argument(
        "remote", help = (
            "Remote URL for the Skills Repository, e.g., "
            "https://github.com/<owner>/<repository>. Check the list "
            "of supported remote sources using 'polyskills sources'."
        )
    )

    manager.add_argument(
        "--pagination", metavar = "[100]", type = int, help = (
            "Set the pagination parameter that controls how many "
            "requests are returned for a 'GET' from the REST API "
            "endpoints, example 'https://api.github.com/repos/...' "
            "which is the endpoint for GitHub."
        )
    )

    manager.add_argument(
        "--token", type = str, help = (
            "Authentication token (typically required for a private "
            "or a self-hosted repository) that is additionally "
            "required for validation. It is recommended not to use "
            "the security token in production system and should only "
            "be used in a testing environment. The token parameter "
            "has a lower precedency and is over written by using an "
            "environment variable 'POLYSKILLS_REMOTE_TOKEN' value."
        )
    )

    # ? Create a Sub-Parser for the ``manager`` Parser
    subskills = manager.add_subparsers(
        dest = "library/skills", required = True, metavar = "LIBRARY"
    )
    skillsmanager = subskills.add_parser(
        "skills", help = (
            "A sub-parser endpoint to manage skills for an LLM Tool. "
            "Typically, the function fetches an 'SKILL.md' file (if "
            "Agents Skills [https://agentskills.io/home]) is supported "
            "else the skill is converted to a prompt. Check the "
            "list of supported LLM tools using 'polyskills tools' "
            "for more details."
        )
    )

    return parser


def main() -> None:
    """
    Entry point for CLI tool for :mod:`polyskills` that parses the
    command line arguments using :mod:`argparse` module.
    """

    args = buildParser().parse_args()

    if hasattr(args, "func") and callable(args.func):
        print(args.func())

    return None

if __name__ == "__main__":
    sys.exit(main())
